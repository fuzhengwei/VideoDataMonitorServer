package com.icss.vdms.webservice.scan.agreement;

public class VedioMonitorMsgScanFeedBack {

	// id
	private String id = "0";
	// 状态【true 成功，false 失败】
	private boolean state = true;
	// 信息
	private String message;

	public VedioMonitorMsgScanFeedBack(){
		
	}
	
	public VedioMonitorMsgScanFeedBack(boolean state){
		this.state = state;
	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public boolean isState() {
		return state;
	}

	public void setState(boolean state) {
		this.state = state;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
